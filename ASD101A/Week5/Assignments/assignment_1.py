

# Define a function called times_ten that accepts a number as an argument.

# The function should return the product of the argument multiplied times 10.
def times_ten(num):
    result = num * 10
    print(f"The product of {num} multiplied by 10 is: {result}")

# Call the times_ten function passing in an argument of 5
times_ten(5)
times_ten(10)
times_ten(15)
times_ten(20)
# ad infinitum